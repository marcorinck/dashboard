define(["angular"], function (angular) {
	"use strict";

	/**
	 *
	 * This module is only an empty placeholder during development.
	 *
	 * When building the project for a server environment, all html templates of the modules folder will be compiled
	 * into this file so templates will be loaded by angularJS with just one http request.
	 *
	 * If you are building a reasonable big application and don't want to load all templates for all users, you'll want
	 * to split this into multiple modules or do it another way.
	 *
	 * see gruntfile.js, ngtemplates Task
	 *
	 */

	return angular.module("templates", []);
});